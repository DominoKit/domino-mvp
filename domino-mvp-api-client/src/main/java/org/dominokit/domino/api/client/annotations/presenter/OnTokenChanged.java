package org.dominokit.domino.api.client.annotations.presenter;

import java.lang.annotation.ElementType;
import java.lang.annotation.Retention;
import java.lang.annotation.RetentionPolicy;
import java.lang.annotation.Target;

/**
 * Use this annotation on a presenter proxy method to be called the URL token that should activate
 * the presenter is changed while the presenter is already active
 */
@Retention(RetentionPolicy.RUNTIME)
@Target(ElementType.METHOD)
public @interface OnTokenChanged {}
